/*
Pattern: Slow and Fast Pointer (Floyd's Cycle Detection)

Time: O(n)
Space: O(1)

Idea:
Move slow by one step and fast by two steps.
If they ever meet, a cycle exists.
If fast reaches null, there is no cycle.
*/

/**
 * Definition for singly-linked list.
 * class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) {
 *         val = x;
 *         next = null;
 *     }
 * }
 */
public class Solution {
    public boolean hasCycle(ListNode head) {
        ListNode fast = head;
        ListNode slow = head;
        while(fast!=null && fast.next!=null){
            slow=slow.next;
            fast=fast.next.next;
            if(slow==fast){
                return true;
            }
        }
        return false;
    }
}